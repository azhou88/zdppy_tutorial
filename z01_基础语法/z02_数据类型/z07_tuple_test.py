# 创建元组
tuple_test = (1, 2, 3)
print(tuple_test)
print(type(tuple_test))
print("-------------------------")

# 创建空元组
tuple_01 = ()
print(type(tuple_01))
print("-------------------------")

# 元组转布尔
print(bool((1,)))
print(len(tuple_01))
print("-------------------------")

# 元组最大值和最小值
max_count = max(tuple_test)
print(max_count)
min_count = min(tuple_test)
print(min_count)
print("-------------------------")
