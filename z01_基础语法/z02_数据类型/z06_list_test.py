# coding:utf-8
# 定义列表
none_list = [None, None, None]

print(none_list)

# 转换为布尔
print(bool(none_list))

# 列表长度
print(len(none_list))

# 空列表
print([])
print(bool([]))

# 列表的最大值和最小值
max_list = [1, 3.14]
print(max_list)
print(max(max_list))
print(min(max_list))

# 列表的地址
id_address = id(max_list)
print(id_address)
