# 将大写和小写反转过来
info = "hello world"
print(info.swapcase())
print("----------------------")

info = "HELLO WORLD"
print(info.swapcase())
print("----------------------")

info = "Hello woRLD"
print(info.swapcase())
print("----------------------")

info = "你好 Hello  世界 woRLD"
print(info.swapcase())
print("----------------------")
