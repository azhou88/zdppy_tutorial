# 作用：将首字母转换为大写， 其他字母变成小写
info = "hello world"
print(info, info.capitalize())
print("------------------------------")

info = "hELLO woRld"
print(info, info.capitalize())
print("------------------------------")

info = "HELLO WORLD"
print(info, info.capitalize())
print("------------------------------")
